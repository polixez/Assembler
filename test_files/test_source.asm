; Тестовая программа: поэлементное сравнение двух векторов длины 4
; Векторы A и B расположены по адресам 0 и 4 соответственно
; Результат сохраняется в вектор B

; Предполагается, что память предварительно инициализирована

; Цикл для i от 0 до 3
; i = 0
LDC 0       ; accumulator = 0 (смещение для A[0])
LD 0        ; accumulator = A[0]
ST 8        ; Сохранить A[0] во временную память
LDC 4       ; accumulator = 4 (адрес B[0])
LD 0        ; accumulator = B[0]
EQ 8        ; accumulator = (A[0] == B[0])
ST 4        ; B[0] = результат

; i = 1
LDC 1
LD 0
ST 9
LDC 5
LD 0
EQ 9
ST 5

; i = 2
LDC 2
LD 0
ST 10
LDC 6
LD 0
EQ 10
ST 6

; i = 3
LDC 3
LD 0
ST 11
LDC 7
LD 0
EQ 11
ST 7